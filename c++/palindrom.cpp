bool isPalindrome(string str){
    int l = 0;
    int h = str.length() - 1;
    while(l<h){
        if(iswalnum(str[l]) && iswalnum(str[h]) && str[l] != str[h]) return false;
        else if(!iswalnum(str[l])) l++;
        else if(!iswalnum(str[h])) h--;
        else {
            l++;h--;
        }
    }

    return true;
}

